package com.aquamorph.frcmanager.models;

public class TeamEventMatches {
	private String comp_level;
	private String match_number;
	private String time_string;

	public String getComp_level() {
		return comp_level;
	}

	public void setComp_level(String comp_level) {
		this.comp_level = comp_level;
	}

	public String getMatch_number() {
		return match_number;
	}

	public void setMatch_number(String match_number) {
		this.match_number = match_number;
	}

	public String getTime_string() {
		return time_string;
	}

	public void setTime_string(String time_string) {
		this.time_string = time_string;
	}
}
